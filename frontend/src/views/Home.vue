<template>
  
  <div class="home text-primary">
    <div class="container mt-2">
      <div v-for="question in questions" :key="question.pk">
        <p class="mb-0">Posted by:
          <span class="text-info">{{ question.author }}</span>
        </p>
        <h2>
          <router-link :to="{ name: 'question', params: { slug: question.slug }}"> 
            {{ question.content }}
          </router-link>
        </h2>
        <p class="w3-text-indigo">Answers: {{ question.answers_count }}</p>
        <hr>
       
      </div>
      <div class="my-4">
        <p v-show="loadingQuestions">...load...</p>
        <button 
          v-show="next"
          @click="getQuestions"
          class="btn btn-sm btn-outline-success"
          >Load more...</button>
      </div>

    </div>
  </div>
</template>

<script>
import { APIService } from "../common/api.service.js";

export default {
  name: "Home",
  data() {
    return {
      questions: [],
      next: null,
      loadingQuestions: false
    }
  },
  methods: {
    getQuestions() {
      // make a GET Request to the questions list endpoint and populate the questions array
      let endpoint = "/api/questions/";
      if(this.next){
        endpoint = this.next;
      }
      this.loadingQuestions = true
      APIService(endpoint)
        .then(data => {
          this.questions.push(...data.results)
          this.loadingQuestions = false;
          if(data.next) {
            this.next = data.next;
          }else {
            this.next = null;
          }
          
        })
    }
  },
  created() {
    this.getQuestions()
    document.title = "QA App";
  }
};
</script>

